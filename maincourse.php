Adjuk hozzá a fehérbort és 3-4 percig főzzük, majd adjuk hozzá a tejszínt  a tisztát, és a titkos összetevőt, a fél bögre tésztavizet!  Ezután ahogy tovább főzzük, észre fogjuk venni, hogy elkezd sűrűsödni. 3-4 perc után vegyük le a tűzről, adjunk hozzá egy kis citromlevet, sajtot és petrezselymet, pici sót és borsot. Óvatosan keverjük össze, és készen is vagyunk!
Tálaláskor dióval és sajttal megszórva díszíthetjük.</div><br/>
