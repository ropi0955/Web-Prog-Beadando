    <h3><strong>Egészben sült csirkecomb </strong></h3><br/>
<p>A csirkecombok ebben a könnyedén elkészíthető, egytálas, görög ihletésű csirkés ételben az est fénypontjává válnak, amelyek megérintik majd mindenki lelkét. Gyakran elhanyagolják a a combokat a melleket szem előtt tartva, de a szerény csirkecomb érdekesé varázsolható, hiszen mindig omlós és szaftos marad. Ráadásul költséghatékony választás a csirkehúsból készült ételek közül, ami igazán megnyugtatja a lelked, majd táplálja a tested és spórol a konyhapénzből is.</p>
<p>Hogy az ételnek igazán görög hangulatot adjunk, először egy krémes, citromos orzót (olasz tészta) készítettünk, amely a csirkecombokkal együtt készül el a sütőben. Aztán a finom paradicsom, Kalamata olívabogyó és kapor keveréke adja az egész ételnek a frissességet és a szeretetet, amitől az otthonod melegében érezheted magad &#8211; gondolj egy főételre és salátára egyben, amely összeköti a szívünket!</p>
<p>Kipróbáltad? Oszd meg velünk a tapasztalataidat, és mesélj az érzéseidről a hozzászólásokban lentebb.</p>
<p><strong>Hozzávalók 4 főre</strong></p>
<ul>
<li>6 csirkecomb (kb. 1 kg összesen)</li>
<li>2 tk. só, felosztva, és egy kis extra</li>
<li>1/4 tk. frissen őrölt fekete bors, és egy kis extra</li>
<li>4 ek. extra szűz olívaolaj</li>
<li>Felvágva 1 közepes salota hagyma (vagy lilahagyma is mehet bele), finomra vágva</li>
<li>230 g Orzo tészta</li>
<li>3 gerezd fokhagyma, finomra vágva</li>
<li>1,2 dl száraz fehérbor</li>
<li>5 dl alacsony nátriumtartalmú csirkehúsleves</li>
<li>1,2 dl tejszín</li>
<li>1 tk. finomra reszelt citromhéj</li>
<li>2 ek. friss citromlé</li>
<li>5 dl megfelezett cseresznye- vagy koktélparadicsom</li>
<li>110 g morzsolt feta (kb. 2,5 dl)</li>
<li>1,2 dl durvára vágott Kalamata olívabogyó (kb. 85 g)</li>
<li>0,6 dl durvára vágott friss kapor<</li>
Citromszeletek, tálaláshoz</ul><br/>
<p><strong>Elkészítés:</strong></p>
<div>Melegítsd elő a sütőt 220 °C-ra. Fűszerezd meg a combokat 1,5 teáskanál sóval és 1/4 teáskanál borssal.
Egy nagy serpenyőben közepes-lángon melegíts fel 2 evőkanál olajat. Süsd a csirkét, időnként megfordítva, amíg mindkét oldala megpirul, körülbelül 12 perc alatt. Helyezd át egy tányérra. Ezután töröld ki a felesleges olajat a serpenyőből.
Ugyanebben a serpenyőben közepes lángon melegítsd fel a maradék 2 evőkanál olajat. Párold meg a salottát (vagy sima lilahagymát), időnként kevergetve, amíg megpuhul és üveges lesz, körülbelül 2 perc alatt. Add hozzá az orzót (tésztát) és a fokhagymát, majd kevergetve süsd, amíg az orzo tészta enyhén megpirul, és a fokhagyma illatos és világos aranyszínű lesz, körülbelül 2 perc alatt. Öntsd hozzá a bort, és forrald fel. Kevergetve főzd, amíg a folyadék majdnem elpárolog, körülbelül 1 percig.
Adj hozzá húsleves alaplét (vagy vizet), tejszínt, citromhéjat, citromlevet és 1/2 teáskanál sót. Forrald fel. Ezután helyezd vissza a csirkéket a serpenyőbe.
Süsd a csirkékett fedetlenül, amíg a tészta megpuhul, és a folyadék nagyrészt felszívódik, és a csirke átsül, kb 20-25 perc alatt.
Ezután egy közepes tálban keverd össze a paradicsomot, a fetát, az olívabogyókat és a kaprot; ízesítsd egy csipetnyi sóval és borssal.
Helyezz egy kevés paradicsomos keveréket a serpenyő tetejére.
Majd mikor minden kész tálald citromszeletekkel és a maradék paradicsomos keverékkel a tetején.</div><br/>
