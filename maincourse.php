<h3><strong>Buffalo csirke paprikába töltve</strong></h3><br/>
<p>A buffalo csirke jellegzetes csípős amerikai étel, főleg a csirkeszárnyat készítik el így, fűszeres, ropogós módon. Kísérleteztünk kicsit mi is, paprikába töltöttük a húst. A végeredmény csodálatos lett.</p>
<p><strong>Hozzávalók</strong></p>
<ul>
<li>4 db paprika</li>
<li>2,5 ej vaj</li>
<li>4 db zöldhagyma</li>
<li>0,5 kg csirkemell darálva</li>
<li>1 bögre (fagyasztott) kukorica</li>
<li>krémsajt</li>
<li>1 bögre reszelt sajtkeverék</li>
<li>1 ek BBQ szósz</li></ul><br/>
<p><strong>Elkészítés:</strong></p>
<div>A paprikát hosszában félbevágjuk és kivesszük a magokat. A tepsit kizsírozzuk, egy nagy serpenyőben pedig elkezdjük melegíteni a vajat. Amikor megolvadt a vaj, adjuk hozzá a felszeletelt hagymát, és lassú kevergetés mellett főzzük 5 percig. Ezek után adjuk hozzá a sajtot, kukoricát, csirkehúst, BBQ szószt és a fokhagymát. Addig főzzük, amíg a sajt teljesen el nem olvas.
Az így kapott keveréket töltsük bele a félbevágott paprikákba és előmelegített sütőben lefedve süssük 25-30 percig. Ezután levesszük a fedőt, majd szórjuk meg egy kis reszelt sajttal és a hagymával, és süssük tovább 5 percig.</div><br/>
