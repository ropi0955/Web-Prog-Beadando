<ul>
<li>2 nagyobb szelet lazac (személyenként kb. 20 dkg)</li>
<li>az öntethez 2 evőkanál olívaolaj, 2 fokhagyma, egy kis ágacska friss rozmaring, egy kevés friss vagy szárított oregánó, só, bors</li>
<li>a grillezett zöldségekhez 3-4 különböző zöldség ízlés szerint, a célra ideális a cukkini, sárgarépa, lilahagyma, húsos paprika, paradicsom, édeskömény, spárga, padlizsán</li>
<li>a tálaláshoz egy fél citrom</li>
</ul>
<p><strong>Elkészítés</strong></p>
<div><p>Az öntethez reszeld le vagy aprítsd fel a fokhagymát és a friss fűszernövényeket. Ezekből körülbelül egy púpos teáskanálnyi mennyiség kell, szárított formában azonban jóval kevesebb, egy csapott kávéskanálnyi is elég. Keverj össze mindent az olajjal, és kend be ezzel a halszeletek mindkét oldalát. A grillezett zöldséghez valókat vágd ujjnyi szeletekre. Egy tepsire teríts sütőpapírt, helyezd középre a halszeleteket, rendezd köréjük a zöldséget. A zöldséget locsold meg egy kevés olívaolajjal, sózd, borsozd ízlés szerint. Csúsztasd a tepsit az előmelegített, 200 fokos sütőbe, és süsd 20-30 percig. A sütési idő függ a lazac vastagságától, és a sütőtálca helyzetétől is. Érdemes 16-18 perc múlva ellenőrizni a halszeletek oldalán, mennyire sültek meg. Ha kiveszed a tálcát, ragadd meg az alkalmat, hogy kissé átforgasd a zöldségeket, hogy egyenletesebben süljenek.</p>
<p>A fogás előnye, hogy a hallal együtt elkészül a köret is! Ha roppanósan szereted a zöldségeket, a lazacot pedig belül rózsaszínűen, a sütéshez 20 perc is elég. Tálaláshoz csepegtess pár csepp citromlevet a lazacra és akár a zöldségekre is.</p></div><br/>
