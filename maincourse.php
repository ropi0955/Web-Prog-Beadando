<p><b><em>1. Rozmaringos lazac grillezett zöldségekkel</em></b></p>
<p>Gyors és egyszerű recept, mégis különleges, és roppant látványos. Különösen, ha élénk színű zöldségekkel köríted a halat: piros kaliforniai paprikával, sárgarépával, zöld spárgával, lilahagyma-szeletekkel. Ha igazi grillen készíted, a kerti partik sztárja lehet.</p>
