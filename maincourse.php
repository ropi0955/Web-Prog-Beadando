<p><strong>Elkészítés:</strong></p>
<div>Ha rozmaringot és diót is használnunk, akkor első lépésben hevítsük fel egy nagy serpenyőbe az olajat közepes lángon és adjuk hozzá a diót és a rozmaringot, majd addig kevergessük, amig aranybarna színt nem kapnak ( kb 3 perc ). Miután kész, egy közepes tábla tett át, és szórd meg egy csipet sóval és citromlével.
Ha az első lépést kihagyjátok, akkor a serpenyőbe vajat olvasszátok fel, adjátok hozzá a gombát, borsot, egy csipet sót és keverjétek össze, és 5 perce fedjük le. Ez azért hasznos, mert ennyi idő elég, hogy a gombában található folyadék kijöjjön. Amikor ez megtörtént, keverjük meg, és közepes lángon időnként megkeverve legalább 5 percig főzzük, amig a gomba aranybarna nem lesz.
A tésztát egy nagy lábos sós vízben főzzük meg, amikor kész öntsük le a vizet, de 1 bögrével tartsunk meg belőle később mutatjuk a trükköt, miért  jó.
